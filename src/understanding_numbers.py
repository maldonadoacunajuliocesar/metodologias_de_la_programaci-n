
# Practica 1 Numbers 

"""
    Integers - Enteros 

    Son números sin punto decimal, 
    -infinity ,..., -2, -1, 0, 1, 2,..., infinity

    Ejemplo: 

     #Tipado dinámico 
        age = 23

     Los podemos suma (+), restar (-), multiplicar (*), dividir (/)

     Potencias (**2, **3, etc)

     Modulo (Diviendo % Divisor) - Nos da el residuo de una división

"""

number_1 = 39
number_2 = 13 

plus = number_1 + number_2
difference = number_1 - number_2
multiplication = number_1 * number_2   
division = number_1 / number_2
modulo = number_1 % number_2
power = number_1 ** 2   

print("Suma: ", plus)
print("Difference: ", difference)
print("Multiplicación: ", multiplication)
print("División: ", division)   
print("Módulo: ", modulo)
print("Potencia: ", power)

print("La suma es el tipo", type(plus))
print("La difference es el tipo", type(difference))
print("La multiplicación es el tipo", type(multiplication))
print("La división es el tipo", type(division))
print("El módulo es el tipo", type(modulo))     
print("La potencia es el tipo", type(power))

